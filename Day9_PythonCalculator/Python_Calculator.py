def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


def calculator():
    should_continue = True
    num1 = float(input("Input your first number: "))
    while should_continue:
        choice = input("Pick a symbol: ")
        num2 = float(input("Input your second number: "))
        function = operations[choice]
        answer = function(num1, num2)
        print(f'{num1} {choice} {num2} = {answer}')

        if input(f'Would you like to perform another operation on {answer}? Type "Y" for Yes and "N" for No. ') == "Y":
            num1 = answer
        else:
            should_continue = False
            calculator()


